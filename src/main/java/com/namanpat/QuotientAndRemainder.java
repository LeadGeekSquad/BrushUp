public class QuotientAndRemainder {
    public static void main(String[] args) {
        int a =100;
        int b = 5;

        int quotient = a/b;
        int remainder = a%b;

        System.out.println("The quotient when dividing " + a + "/" + b + " = " + quotient + "\n" +
                            "The remainder when dividing " + a + "/" + b + " = " + remainder);
    }
}
