public class VowelOrConsonant {
    public static void main(String[] args) {

        char abet = 'n';

        if (abet == 'a' || abet == 'e' || abet == 'i' || abet == 'o' || abet == 'u') {
            System.out.println(abet + " is a vowel.");
        } else {
            System.out.println(abet + " is a Consonant.");
        }
    }
}
